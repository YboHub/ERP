package com.aaa.entity.warehouseentity;

public class Food {
	private Integer id;
	private Integer warehousenum;
	private String warehousename;
	private String warehousetype;
	private String warehousekeeper;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getWarehousenum() {
		return warehousenum;
	}
	public void setWarehousenum(Integer warehousenum) {
		this.warehousenum = warehousenum;
	}
	public String getWarehousename() {
		return warehousename;
	}
	public void setWarehousename(String warehousename) {
		this.warehousename = warehousename;
	}
	public String getWarehousetype() {
		return warehousetype;
	}
	public void setWarehousetype(String warehousetype) {
		this.warehousetype = warehousetype;
	}
	public String getWarehousekeeper() {
		return warehousekeeper;
	}
	public void setWarehousekeeper(String warehousekeeper) {
		this.warehousekeeper = warehousekeeper;
	}
	
}
