package com.aaa.entity.manger;

public class Capacity {
private Integer cid;//编号

private String cnumber;//产品编号

private String cmin;//正常的产量

private String cmax;//最大的产量

private String cdan;//单位

private String cxian;//产线

public Integer getCid() {
	return cid;
}

public void setCid(Integer cid) {
	this.cid = cid;
}

public String getCnumber() {
	return cnumber;
}

public void setCnumber(String cnumber) {
	this.cnumber = cnumber;
}

public String getCmin() {
	return cmin;
}

public void setCmin(String cmin) {
	this.cmin = cmin;
}

public String getCmax() {
	return cmax;
}

public void setCmax(String cmax) {
	this.cmax = cmax;
}

public String getCdan() {
	return cdan;
}

public void setCdan(String cdan) {
	this.cdan = cdan;
}

public String getCxian() {
	return cxian;
}

public void setCxian(String cxian) {
	this.cxian = cxian;
}


}
