public class Formula {
	private Integer pid;//id
	private String ptype;//类型
	private String pnumber;//编号
	private String pname;//名称
	private String pgui;//规格
	private Double pshu;//数量
	private String pdan;//订单状态
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public String getPtype() {
		return ptype;
	}
	public void setPtype(String ptype) {
		this.ptype = ptype;
	}
	public String getPnumber() {
		return pnumber;
	}
	public void setPnumber(String pnumber) {
		this.pnumber = pnumber;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPgui() {
		return pgui;
	}
	public void setPgui(String pgui) {
		this.pgui = pgui;
	}
	
	public Double getPshu() {
		return pshu;
	}
	public void setPshu(Double pshu) {
		this.pshu = pshu;
	}
	public String getPdan() {
		return pdan;
	}
	public void setPdan(String pdan) {
		this.pdan = pdan;
	}
	
	
	
}
